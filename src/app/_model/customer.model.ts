/**
 * The details of the customer.
 */
export interface Customer {
  /**
   * The given names of the customer.
   */
  givenNames: string;

  /**
   * The surname of the customer.
   */
  surname: string;

  /**
   * The phone number of the customer.
   */
  phoneNumber: string;

  /**
   * The email of the customer.
   */
  email: string;
}
